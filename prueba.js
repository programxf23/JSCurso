//condicional switch

var color = prompt("Ingresa un color");

switch (color) {

    case "rojo":
        console.log("Rojo");
        break;

    case "azul":
        console.log("Azul");
        break;

    case "verde":
        console.log("Verde");
        break;

    default:
        console.log("Color no valido");
        break;
}